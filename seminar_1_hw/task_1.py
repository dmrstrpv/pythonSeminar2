# Нарисовать в консоли ёлку спросив
# у пользователя количество рядов.
# Пример результата:
# Сколько рядов у ёлки? 5
#     *
#    ***
#   *****
#  *******
# *********

SPACE = " "
ASTERISK = "*"
count = 0
rows_num = int(input("Сколько рядов у ёлки?: "))

for i in range(rows_num):
    print(SPACE * (rows_num - i - 1) + ASTERISK * (i * 2 + 1))
